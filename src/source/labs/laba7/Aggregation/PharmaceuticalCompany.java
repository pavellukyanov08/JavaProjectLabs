package source.labs.laba7.Aggregation;

public class PharmaceuticalCompany {
    String region;
    String city;
    String street;
    String comp_name;

    PharmaceuticalCompany (String region, String city, String street, String comp_name) {
        this.region = region;
        this.city = city;
        this.street = street;
        this.comp_name = comp_name;
    }
}
